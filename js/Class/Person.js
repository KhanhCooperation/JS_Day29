
class Person {
    constructor(ten, addr, id, email){
        this.ten=ten;
        this.address=addr;
        this.id=id;
        this.email=email;
    }
    getName(){
        return this.ten=ten;
    }
    getAddress(){
        return this.address  
    }
    getID(){
        return this.id;
    }
    getEmail(){
        return this.email;
    }
}